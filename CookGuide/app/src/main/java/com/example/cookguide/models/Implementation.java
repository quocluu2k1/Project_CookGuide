package com.example.cookguide.models;

public class Implementation {
    public int stepOrder;
    public String impDetail;
    public String stepImage;
    public int time;

    public Implementation(){}

    public Implementation(int stepOrder, String impDetail, String stepImage, int time) {
        this.stepOrder = stepOrder;
        this.impDetail = impDetail;
        this.stepImage = stepImage;
        this.time = time;
    }
}
